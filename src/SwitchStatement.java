public class SwitchStatement {
    public static void main(String[] args){
        String colorModeSelected = "Dark";

        switch (colorModeSelected){
            case "Light":
                System.out.println("Seleccionante " + colorModeSelected + " mode");
                break;
            case "Dark":
                System.out.println("Seleccionante " + colorModeSelected + " mode");
                break;
            case "Night":
                System.out.println("Seleccionante " + colorModeSelected + " mode");
                break;
        }
    }
}
